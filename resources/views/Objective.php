        <p>Project Management has developed in order to plan, co-ordinate and control the complex and diverse <br>activities of modern industrial and commercial projects. All projects share one common characteristic - the  <br>projection of ideas and activities into new endeavours.</p>
        </div>
        <div>
        <p>The purpose of project management is to foresee or predict as many dangers and problems as possible; and to plan, <br> organise and control activities so that the project is completed as successfully as possible in spite of all the risks.  <br>The ever-present element of risk and uncertainty means that events and tasks leading to completion can never be <br>foretold with absolute accuracy. For some complex or advanced projects, even the possibility of successful <br> completion might be of serious doubt.</p>
        </div>
        <div>
        <p>Project management can involve the following activities: planning - deciding what is to be done; organising - making  <br>arrangements; staffing - selecting the right people for the job; directing - giving instructions; monitoring - checking  on  <br>progress; controlling - taking action to remedy hold ups; innovation - coming up with new solutions; representing -  <br>liaising with users.</p>
        </div>
